package com.toddev.bechdel;

/**
 * Created by David Margolin on 5/8/2016.
 * Items from the bechdeltest database
 */
public class ResultItems {
    private String dubious;
    private String imdbid;
    private String id;
    private String title;
    private String year;
    private String visible;
    private String date;
    private String submitterid;
    private String rating;

    public String getYear() {
        return year;
    }

    public void setYear(String year) {
        this.year = year;
    }

    public String getVisible() {
        return visible;
    }

    public void setVisible(String visible) {
        this.visible = visible;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getSubmitterid() {
        return submitterid;
    }

    public void setSubmitterid(String submitterid) {
        this.submitterid = submitterid;
    }

    public String getRating() {
        return rating;
    }

    public void setRating(String rating) {
        this.rating = rating;
    }

    public String getDubious() {
        return dubious;
    }

    public void setDubious(String dubious) {
        this.dubious = dubious;
    }

    public String getImdbid() {
        return imdbid;
    }

    public void setImdbid(String imdbid) {
        this.imdbid = imdbid;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

}
